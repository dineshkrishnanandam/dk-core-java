package com.kani.myclass;

public class Square {

	/*
	 * side is a member variable that holds value of side
	 */
	int side;

	
	
	/*
	 * calculateArea() is called as method or function.
	 * it finds area of the square returns area
	 */
	int calculateArea() {

		int area = side * side;
		return area;

	}
}
